using namespace std;
using namespace Eigen;
// Подставляем все в начальное уравнение и логарифмируем его с целью перехода к линейному уравнению
// y = A * x + B, где y = lg(Z), x = T

// Структура для хранения пары "Температура - Показание индикатора"
struct Measure {
    double t;           // Температура, соответствующая показанию индикатора
    double z;           // Показание индикатора (АЦП)
};
// Функция МНК (метод наименьших квадратов)
// Ищем коэффициенты без отбрасывания выбросов
VectorXd solveOLS(const vector<Measure>& data) {   // Функция возвращает вектор с коэффициентами A и B
    int n = data.size();                           // Узнаем, сколько всего точек измерений мы считали из файла
    // Матрица системы размером N * 2: два столбца нужны, так как мы ищем два параметра линейного уравнения
    // Один столбец для переменной T, второй столбец для свободного члена B
    MatrixXd A(n, 2);
    // Вектор правых частей системы размером N
    VectorXd b(n);
    // Заполняем матрицу и вектор
    for (int i = 0; i < n; ++i) {
        A(i, 0) = data[i].t;       // В первый столбец матрицы записываем температуру
        A(i, 1) = 1.0;             // Во второй столбец матрицы записываем 1, это следует из y = A * x + B * 1, где x = T
        b(i) = log10(data[i].z);   // Линеаризуем уравнение, как и планировалось, логарифмируя показания индикатора Z
    }
    // Решаем уравнение методом QR-разложения
    return A.householderQr().solve(b);
}
// Используем функцию М-оценки (M-estimator) из лекции 6 для борьбы с выбросами
pair<VectorXd, VectorXd> solveMEstimator(const vector<Measure>& data) {   // Функция возвращает A, B и веса точек
    // Заполнение матрицы и вектора аналогично МНК
    int n = data.size();
    MatrixXd A(n, 2);
    VectorXd b(n);
    for (int i = 0; i < n; ++i) {
        A(i, 0) = data[i].t;
        A(i, 1) = 1.0;
        b(i) = log10(data[i].z);
    }
    // Изначально все точки считаются равноправными, поэтому записываем единичные веса
    VectorXd weights = VectorXd::Ones(n);
    VectorXd x(2);          // Вектор для искомых A и B
    double delta = 0.001;   // Маленькое значение для защиты от деления на ноль, если ошибка (невязка) окажется нулевой
    // Итерационный процесс пересчета весов (M-estimator)
    // Он решает систему, смотрит на ошибки, уменьшает значимость (вес) точек с большими ошибками (выбросы) и решает систему заново
    // Запускаем цикл на 100 шагов (этого количества итераций достаточно, чтобы веса установились и система сошлась)
    for (int iter = 0; iter < 100; ++iter) {
        // Создаем диагональную матрицу весов
        MatrixXd W = weights.asDiagonal();
        // Решаем систему методом LU-разложения: (A^T * W * A) * x = A^T * W * b
        x = (A.transpose() * W * A).partialPivLu().solve(A.transpose() * W * b);
        // Считаем невязку (ошибку) для каждой точки: насколько модель отошла от факта
        VectorXd residuals = b - A * x;
        // Пересчитываем веса: чем больше ошибка, тем меньше вес (1/ошибка)
        for (int i = 0; i < n; ++i) {
            weights(i) = 1.0 / max(delta, abs(residuals(i)));
        }
    }
    return {x, weights};   // Возвращаем вектор коэффициентов A и B, а также итоговые веса точек
}

int main() {
    // Настройка кодировки для корректного вывода
    setlocale(LC_ALL, "Russian");   // Устанавливает русскую локаль для стандартных потоков C++
    system("chcp 1251 > nul");      // Cкрывает системное сообщение об успешной смене кодировки, чтобы не выводить это на экран
    // Читаем данные из файла
    vector<Measure> data;
    ifstream fin("Task2_data.csv");
    
    double t, z;
    // Цикл чтения: игнорирует запятые и точки с запятой
    while (fin >> t) {
        if (fin.peek() == ',' || fin.peek() == ';') fin.ignore();
        if (fin >> z) data.push_back({t, z});
    }
    fin.close();
    // Проверяем, успешно ли открылся файл
    if (data.empty()) {
        cout << "Ошибка: Данные не загружены." << endl;
        return 1;    // Завершаем программу с кодом 1 (признак ошибки)
    }
    
    VectorXd paramsOLS = solveOLS(data);    // Считаем простым методом
    auto resultM = solveMEstimator(data);   // Считаем устойчивым методом
    VectorXd paramsM = resultM.first;       // Коэффициенты A и B
    VectorXd weights = resultM.second;      // Веса (нужны для поиска выбросов)
    // Считаем порог: точка считается выбросом, если её вес в 10 раз меньше среднего
    int outliersCount = 0;
    double weightThreshold = weights.mean() * 0.1;
    
    cout << fixed << setprecision(6);
    cout << "=== СРАВНЕНИЕ ПАРАМЕТРОВ МОДЕЛЕЙ ===" << endl;
    cout << "МНК (без отбрасывания выбросов): A = " << paramsOLS(0) << ", B = " << paramsOLS(1) << endl;
    cout << "M-оценка (с отбрасыванием выбросов): A = " << paramsM(0) << ", B = " << paramsM(1) << endl;
    
    cout << "\n=== АНАЛИЗ ВЫБРОСОВ ===" << endl;
    for (int i = 0; i < data.size(); ++i) {
        if (weights(i) < weightThreshold) {   // Если вес мал (выброс)
            outliersCount++;
            cout << "Выброс #" << outliersCount << ": T=" << data[i].t << ", Z=" << data[i].z << endl;
        }
    }
    cout << "Всего обнаружено выбросов: " << outliersCount << " из " << data.size() << endl;
    
    cout << "\n=== РАСЧЕТ ТЕМПЕРАТУРЫ ===" << endl;
    cout << "Введите показание Z от 1 до 1023 (0 для выхода):" << endl;
    double inputZ;
    while (true) {
        cout << "\nZ = ";
        if (!(cin >> inputZ) || inputZ <= 0 || inputZ > 1023) break;
        
        double logZ = log10(inputZ);
        double T_OLS = (logZ - paramsOLS(1)) / paramsOLS(0);
        double T_M = (logZ - paramsM(1)) / paramsM(0);
     
        cout << "-------------------------------------------" << endl;
        cout << "Вариант 1 (МНК):    T = " << setw(10) << T_OLS << " C" << endl;
        cout << "Вариант 2 (M-оценка.):  T = " << setw(10) << T_M << " C" << endl;
        cout << "Разница подходов:   " << setw(10) << abs(T_OLS - T_M) << " C" << endl;
        cout << "-------------------------------------------" << endl;
    }
 
    return 0;
}